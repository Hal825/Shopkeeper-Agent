-- ===========================================
-- 扩展 dim_date（2025-04-01 至 2025-12-31）
-- 使用存储过程批量生成，确保日期连续且格式正确
-- ===========================================
DELIMITER $$
CREATE PROCEDURE generate_dim_date_fix()
BEGIN
    DECLARE d DATE DEFAULT '2025-04-01';
    DECLARE end_d DATE DEFAULT '2025-12-31';
    WHILE d <= end_d DO
        INSERT IGNORE INTO dim_date (date_id, year, quarter, month, day)
        VALUES (DATE_FORMAT(d, '%Y%m%d'), YEAR(d), CONCAT('Q', QUARTER(d)), MONTH(d), DAY(d));
        SET d = DATE_ADD(d, INTERVAL 1 DAY);
    END WHILE;
END$$
DELIMITER ;
CALL generate_dim_date_fix();
